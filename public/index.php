header('Access-Control-Allow-Origin: *');
header('Access-Control-Allow-Methods: GET, HEAD, PUT, POST, DELETE, PATCH, OPTIONS');
header('Access-Control-Allow-Credentials: ture');
header('Access-Control-Max-Age: 864000');
header('Access-Control-Allow-Headers: Content-Type, Authorization, Origin, Accept, X-Requested-With');
header('Content-Type: application/json');
$method = $_SERVER['REQUEST_METHOD'];
if ($method == "OPTIONS") {
    header("Access-Control-Allow-Headers: X-API-KEY, Origin, X-Requested-With, Content-Type, Accept, Access-Control-Request-Method,Access-Control-Request-Headers, Authorization");
    header("HTTP/1.1 200 OK");
